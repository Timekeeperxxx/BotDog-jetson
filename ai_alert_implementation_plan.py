# ============================================================================
# 1. AI 推理集成（温度检测）
# ============================================================================

## 1.1 温度监控模块
# 文件：backend/temperature_monitor.py
# 功能：
#   - 监听 MAVLink NAMED_VALUE_FLOAT 消息
#   - 检测 T_MAX 值
#   - 当 T_MAX > thermal_threshold 时触发告警

## 1.2 截图功能
# 文件：backend/snapshot_service.py
# 功能：
#   - 从视频流中截取当前帧
#   - 保存到 /data/snapshots/{date}/{task_id}/
#   - 返回文件路径和 GPS 坐标

# ============================================================================
# 2. 后端告警处理
# ============================================================================

## 2.1 告警生成
# 文件：backend/alert_service.py
# 功能：
#   - 接收温度告警触发
#   - 获取当前 GPS 位置
#   - 生成证据记录到数据库
#   - 触发 WebSocket 广播

## 2.2 事件 WebSocket
# 端点：/ws/event
# 功能：
#   - 广播 ALERT_RAISED 消息
#   - 推送实时告警到前端

# ============================================================================
# 3. 前端实时抓拍
# ============================================================================

## 3.1 事件订阅 Hook
# 文件：frontend/src/hooks/useEventWebSocket.ts
# 功能：
#   - 订阅 /ws/event
#   - 接收 ALERT_RAISED 消息
#   - 更新抓拍列表

## 3.2 抓拍列表展示
# 文件：frontend/src/components/SnapshotList.tsx (已有基础)
# 功能：
#   - 显示实时抓拍
#   - 显示时间戳、置信度、事件类型

# ============================================================================
# 4. 历史证据查询
# ============================================================================

## 4.1 历史页面
# 文件：frontend/src/pages/EvidenceHistory.tsx
# 功能：
#   - 调用 GET /api/v1/evidence
#   - 显示历史抓拍列表
#   - 支持按 task_id 筛选

# ============================================================================
# 实现顺序
# ============================================================================

